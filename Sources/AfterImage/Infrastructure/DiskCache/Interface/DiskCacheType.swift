import Foundation

/// 디스크 기반 캐시가 따라야 하는 공용 인터페이스입니다.
///
/// `Data`를 키 기반으로 저장하고, 필요 시 조회/삭제할 수 있도록 정의합니다.
/// 구현체는 TTL(Time To Live), 개수 제한, 총 용량 제한 같은 정책을 함께 가질 수 있습니다.
///
/// - Note:
///   - 디스크 접근은 메모리 접근보다 비용이 크기 때문에 모든 API를 `async`로 정의했습니다.
///   - 동시성 환경에서 안전하게 전달될 수 있도록 `Sendable`을 채택합니다.
public protocol DiskCacheType: Sendable {
    
    /// 주어진 키에 해당하는 데이터를 조회합니다.
    ///
    /// 처리 흐름은 다음과 같습니다.
    /// 1. 캐시 디렉터리가 존재하는지 확인합니다.
    /// 2. 키를 해시 파일명으로 변환합니다.
    /// 3. 메타데이터를 읽고 만료 여부를 확인합니다.
    /// 4. 유효하면 실제 데이터 파일을 읽어 반환합니다.
    /// 5. 성공 시 마지막 접근 시각을 갱신합니다.
    ///
    /// 조회 중 파일 손상이나 메타데이터 오류가 발생하면,
    /// 해당 항목은 깨진 캐시로 간주하고 제거를 시도한 뒤 `nil`을 반환합니다.
    ///
    /// - Parameter key: 조회할 캐시 키입니다.
    /// - Returns: 유효한 캐시 데이터가 있으면 `Data`, 없으면 `nil`
    func data(key: String) async -> Data?
    
    /// 데이터를 디스크에 저장하고 필요하면 정리 작업을 수행합니다.
    ///
    /// 저장 시
    /// - 실제 바이너리 데이터는 `.data`
    /// - 항목 정보는 `.json`
    /// 으로 분리 저장됩니다.
    ///
    /// TTL은 전달된 값이 있으면 그것을 사용하고,
    /// 없으면 `DiskCacheConfiguration.defaultTTL`을 사용합니다.
    ///
    /// 저장이 끝난 뒤에는 만료 항목 제거 및 LRU 기반 정리를 수행해
    /// 개수 제한과 총 용량 제한을 만족하도록 맞춥니다.
    ///
    /// - Parameters:
    ///   - data: 저장할 데이터입니다.
    ///   - key: 데이터 식별용 키입니다.
    ///   - ttl: 항목별 TTL입니다. `nil`이면 기본 TTL을 사용합니다.
    /// - Throws: 디렉터리 생성, 파일 저장, 메타데이터 저장, 정리 작업 중 오류가 발생할 수 있습니다.
    func insert(_ data: Data, key: String, ttl: TimeInterval?) async throws
    
    /// 주어진 키에 해당하는 캐시 데이터를 삭제합니다.
    ///
    /// 데이터 파일과 메타데이터 파일을 함께 제거합니다.
    ///
    /// - Parameter key: 삭제할 캐시 키입니다.
    /// - Throws: 파일 삭제 실패 시 오류를 throw합니다.
    func removeData(key: String) async throws
    
    
    /// 디스크 캐시에 저장된 모든 항목을 삭제합니다.
    ///
    /// 캐시 디렉터리 내부 파일을 전부 순회하며 제거합니다.
    /// 디렉터리가 존재하지 않으면 아무 작업도 하지 않습니다.
    ///
    /// - Throws: 디렉터리 조회 또는 파일 삭제 중 오류가 발생할 수 있습니다.
    func removeAll() async throws
}

